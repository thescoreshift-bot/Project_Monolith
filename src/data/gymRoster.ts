import type { ElementType } from './starters'

type GymNpcTemplate = {
  id: string
  name: string
  type: ElementType
  kind: 'trainer' | 'boss'
  level: number
  maxHp: number
  stats: { atk: number; def: number; spAtk: number; spDef: number; spd: number }
  abilityIds: string[]
  recruitable: false
  companionTemplateId: string
}

function trainer(
  id: string,
  name: string,
  type: ElementType,
  level: number,
  maxHp: number,
  stats: GymNpcTemplate['stats'],
  abilityIds: string[],
  companionTemplateId: string,
): GymNpcTemplate {
  return {
    id,
    name,
    type,
    kind: 'trainer',
    level,
    maxHp,
    stats,
    abilityIds,
    recruitable: false,
    companionTemplateId,
  }
}

function leader(
  id: string,
  name: string,
  type: ElementType,
  level: number,
  maxHp: number,
  stats: GymNpcTemplate['stats'],
  abilityIds: string[],
  companionTemplateId: string,
): GymNpcTemplate {
  return {
    id,
    name,
    type,
    kind: 'boss',
    level,
    maxHp,
    stats,
    abilityIds,
    recruitable: false,
    companionTemplateId,
  }
}

