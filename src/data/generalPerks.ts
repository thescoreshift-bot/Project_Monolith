import type { ElementType } from './starters'
import type { Perk, PerkCategory, PerkRarity, PerkRole } from './perks'
import { registerPerkTag } from './perkCombatConfig'

type GeneralPerkDraft = {
  id: string
  name: string
  rarity: PerkRarity
  category: PerkCategory
  description: string
  effect: string
  combatTag: string
  weight?: number
  maxStacks?: number
  stackGroup?: string
  unique?: boolean
  allowedTypes?: ElementType[]
  allowedRoles?: PerkRole[]
  statModifiers?: Perk['statModifiers']
  secondaryEvolutionPath?: Perk['secondaryEvolutionPath']
}

function gp(draft: GeneralPerkDraft): Perk {
  registerPerkTag(draft.id, draft.combatTag)
  const { combatTag: _tag, ...rest } = draft
  return { ...rest, weight: draft.weight ?? 10 }
}

